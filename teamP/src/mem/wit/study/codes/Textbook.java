package mem.wit.study.codes;

public class Textbook {
	int tbCode;
	int pCode;
	int sbjCode;
	String tbName;
	int tbPrice;
	
	public int getTbCode() {
		return tbCode;
	}
	public void setTbCode(int tbCode) {
		this.tbCode = tbCode;
	}
	public int getpCode() {
		return pCode;
	}
	public void setpCode(int pCode) {
		this.pCode = pCode;
	}
	public int getSbjCode() {
		return sbjCode;
	}
	public void setSbjCode(int sbjCode) {
		this.sbjCode = sbjCode;
	}
	public String getTbName() {
		return tbName;
	}
	public void setTbName(String tbName) {
		this.tbName = tbName;
	}
	public int getTbPrice() {
		return tbPrice;
	}
	public void setTbPrice(int tbPrice) {
		this.tbPrice = tbPrice;
	}
	
}
