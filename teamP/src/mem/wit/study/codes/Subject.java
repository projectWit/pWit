package mem.wit.study.codes;

public class Subject {
	int sbjCode;
	int sgCode;
	int sbjCatCode;
	String sbjName;
	int sbjYear;
	
	public int getSbjCode() {
		return sbjCode;
	}
	public void setSbjCode(int sbjCode) {
		this.sbjCode = sbjCode;
	}
	public int getSgCode() {
		return sgCode;
	}
	public void setSgCode(int sgCode) {
		this.sgCode = sgCode;
	}
	public int getSbjCatCode() {
		return sbjCatCode;
	}
	public void setSbjCatCode(int sbjCatCode) {
		this.sbjCatCode = sbjCatCode;
	}
	public String getSbjName() {
		return sbjName;
	}
	public void setSbjName(String sbjName) {
		this.sbjName = sbjName;
	}
	public int getSbjYear() {
		return sbjYear;
	}
	public void setSbjYear(int sbjYear) {
		this.sbjYear = sbjYear;
	}
	
}
