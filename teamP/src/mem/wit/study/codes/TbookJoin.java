package mem.wit.study.codes;

public class TbookJoin {
	int tbCode;
	String pName;
	String tbName;
	String sbjName;
	String cName;
	int tbPrice;
	
	public int getTbCode() {
		return tbCode;
	}
	public void setTbCode(int tbCode) {
		this.tbCode = tbCode;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getTbName() {
		return tbName;
	}
	public void setTbName(String tbName) {
		this.tbName = tbName;
	}
	public String getSbjName() {
		return sbjName;
	}
	public void setSbjName(String sbjName) {
		this.sbjName = sbjName;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public int getTbPrice() {
		return tbPrice;
	}
	public void setTbPrice(int tbPrice) {
		this.tbPrice = tbPrice;
	}
	
}
